text: str = "     python    "

# Trim the white space
print("Removing white space : ", text.strip())

# Upper case
print("Upper case ", text.upper())

# Lower Case
print("Lower case ", text.lower())

# Capitalize 
print("Capitalize ", text.capitalize())

# join
print("Join : ", "".join([str(value) for value in [1, 2, 3]]))

# concat
print("This " + "is")