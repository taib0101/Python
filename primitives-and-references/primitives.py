# primitive values are called primitive data types
# mutable means changeable
# immutable means non changeable

x: int = 42                # int (immutable and premitive)
pi: float = 3.14           # float
is_valid: bool = True      # bool
name: str = "Alice"        # string
empty: None = None         # NoneType
data: bytes = b"hello"     # bytes

print("x : ", x)
print("pi : ", pi)
print("is_valid : ", is_valid)
print("name : ", name)
print("empty : ", empty)
print("data : ", data)