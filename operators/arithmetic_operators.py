# Addition
a: int = 10
b: int = 5
sum_result: int = a + b
print(f"Addition: {a} + {b} = {sum_result}")

# Subtraction
diff_result: int = a - b
print(f"Subtraction: {a} - {b} = {diff_result}")

# Multiplication
product_result: int = a * b
print(f"Multiplication: {a} * {b} = {product_result}")

# Division
div_result: int = a / b
print(f"Division: {a} / {b} = {div_result}")

# Floor Division
floor_div_result: int = a // b
print(f"Floor Division: {a} // {b} = {floor_div_result}")

# Modulus (Remainder)
mod_result: int = a % b
print(f"Modulus: {a} % {b} = {mod_result}")

# Exponentiation
exp_result: int = a ** b
print(f"Exponentiation: {a} ** {b} = {exp_result}")