a: int = 10
b: int = 5

# Equal to
print(f"[1, 2, 3] == [1, 2, 3] ➝ {[1, 2, 3] == [1, 2, 3]}")

# Not equal to
print(f"{a} != {b} ➝ {a != b}")

# Greater than
print(f"{a} > {b} ➝ {a > b}")

# Less than
print(f"{a} < {b} ➝ {a < b}")

# Greater than or equal to
print(f"{a} >= {b} ➝ {a >= b}")

# Less than or equal to
print(f"{a} <= {b} ➝ {a <= b}")